	qdotsign(3) = -0.046246530774813;
	qdotsign(4) = -0.011593577210853;
	qdotsign(5) = 3.679302830903592e-04;
	qdotsign(6) = 0.037109762484126;
	qdotsign(7) = 0.054060356332795;

	alpha(1) = 1.957926305620739e+02;
	alpha(2) = 52.849323228714450;
